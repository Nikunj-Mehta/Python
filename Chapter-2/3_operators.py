# Arithmetic Operators
a = 34
b = 4
c = a+b
print(c)

# Assignment Operators
a = 4-2 # Assign 4-2 in a
print(a)
b = 6
b += 3 # Increment the value of b and then assign it to b
print(b)

# Comparision Operators (always returns a boolean value)
d = 5 < 4
print(d)

g = 4 != 5
print(g)

# Logical Operator
e = True or False
print(e)

print(not(False))